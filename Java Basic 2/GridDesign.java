// To make a grid design using '-' 


class GridDesign{

	public static void main(String[] args){

		int i,j;

		for(i=0;i<=10;i++){	// i loop till any value depending on size

			for(j=0;j<=30;j++){	// j loop till any size too

				System.out.print("-");	// element to print
			}	 
			System.out.println("");	// breaking to next line
		}
	}
}
